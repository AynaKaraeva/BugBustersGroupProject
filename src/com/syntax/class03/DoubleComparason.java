package com.syntax.class03;

public class DoubleComparason {

	public static void main(String[] args) {
		
		/*
		 * Create a Java program and name it Double Comparison. 
		 * Declare 2 double values and if value of first variable is higher
		 *  then the second, print “Double value __ is larger than __ .” Otherwise print...
		 */

		double hr = 13;
		double co = 7;

		if (hr > co) {
			System.out.println("Double value " + hr + " is larger than" + co);
		} else {
			System.out.println("Double value " + hr + " is smaller than" + co);
		}

	}

}
